#include "libft.h"

void	*memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*cr;

	i = 0;
	cr = (char *)s;
	while (i < n)
	{
		cr[i] = c;
		i++;
	}
	return (s);
}
