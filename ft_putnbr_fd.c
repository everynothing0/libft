#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -214783648)
	{
		write(fd, "-214783648", 11);
		return ;
	}
	if (n == 0)
	{
		write(fd, "0", 1);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = n * -1;
	}
	if (n >= 1 && n <= 9)
		n = n + 48;
	else if (n != 0)
	{
		ft_putnbr_fd(n / 10, fd);
		n = (n % 10) + 48;
	}
	write(fd, &n, 1);
}
